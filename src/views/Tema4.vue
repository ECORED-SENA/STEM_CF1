          p El concepto de innovación, según Zaltman y otros (1973), hace referencia a tres usos relacionados entre sí. Innovación con relación a “una invención”, es decir, al proceso creativo por el cual dos o más conceptos existentes o entidades son combinados en una forma novedosa, para producir una configuración desconocida previamente. En segundo lugar, la innovación es descrita como el proceso por el cual una innovación existente llega a ser parte del estado cognitivo de un usuario y de su repertorio conductual. Y, por último, una innovación es una idea, una práctica o un artefacto material que ha sido inventado o que es contemplado como novedad, independientemente de su adopción o no adopción. De este modo, el concepto de innovación aparecerá relacionado a estos tres usos: la creación de algo desconocido, la percepción de lo creado como algo nuevo y la asimilación de ese algo como novedoso. La innovación educativa aparece mucho más ligada a los dos últimos usos, por ejemplo, el uso del trabajo grupal como estrategia de enseñanza y el uso de las nuevas tecnologías de la información y la comunicación, respectivamente.

